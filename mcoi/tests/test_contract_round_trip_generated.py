def _round_trip(record, record_type):
    """Serialize then deserialize and assert equality."""
    json_str = serialize_record(record)
    restored = deserialize_record(json_str, record_type)
    assert restored == record, f"Round-trip failed for {record_type.__name__}"
    # Double round-trip: serialize again and verify determinism
    json_str2 = serialize_record(restored)
    assert json_str2 == json_str, f"Deterministic serialization failed for {record_type.__name__}"


